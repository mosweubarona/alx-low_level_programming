#include "3-calc.h"

/**
 * op_add - calculates sum of two int
 * @a: int one
 * @b: int two
 * Return: sum of ints
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates diff of two int
 * @a: int one
 * @b: int two
 * Return: diff of ints
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates product of two int
 * @a: int one
 * @b: int two
 * Return: product of ints
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates divicion of two int
 * @a: int one
 * @b: int two
 * Return: division of two ints
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculates reminder of two int
 * @a: int one
 * @b: int two
 * Return: reminder of two ints
 */
int op_mod(int a, int b)
{
	return (a % b);
}
